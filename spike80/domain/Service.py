class Service:
    def __int__(self, id_service, id_job, id_hostage):
        self.id_service = id_service
        self.id_job = id_job
        self.id_hostage = id_hostage

    def getId_service(self):
        return self.id_service

    def getId_job(self):
        return self.id_job

    def getId_hostage(self):
        return self.id_hostage


